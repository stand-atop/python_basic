# 숫자와 문자열

# 숫자
my_name = 'Python' # 문자열(사람을 위한 텍스트를 프로그래머가 부르는 방법)
my_age = 100 # 숫자
    #my_age = '100' /문자는 연산에러나지
print(my_name, '는 나중에', my_age, '살.. 까지 살긴 너무 길다.')

# 변수를 이용한 연산
my_real_age = my_age - 99
print('진짜 나이는', my_real_age, '살 ㅋㅋ')

# 연산
multiply = 9 * 9 # = 81 / 곱하기
divide = 30 / 5 # = 6 / 나누기
power = 2 ** 10 # = 1026 / 제곱
reminder = 15 % 4 # = 3 / 나머지
print(multiply, divide, power, reminder)

# 문자와 숫자의 연산
text = '2020' + '6618'
number = 2020 + 6618
print(text, number) # 20206618, 8638

